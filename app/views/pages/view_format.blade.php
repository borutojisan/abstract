<strong><p>Bahan Pameran</p></strong>
<ol>
  <li><p>Peserta  adalah digalakkan menyedia dan mempamerkan model atau prototaip atau sample  atau <em>laptop presentation </em>dan lain-lain yang berkenaan dengan penyertaan  untuk memudahkan pemahamam para pengunjung dan panel hakim tentang penyertaan  peserta. Setiap penyertaan akan diberikan satu ruang yang terhad untuk  mempamerkan produk masing-masing.  </p></li>
  <li><p>Kemudahan  yang disediakan penganjur adalah: </p>
    <ol>
      <li><p>Ruang  pameran berukuran (3&rsquo; x 2&rsquo;) x 2.5m (H) (berkongsi)</p></li>
      <li><p>Meja  dan kerusi (2 unit)</p></li>
      <li><p>Sumber  elektrik dan wayar sambungan (Extension)</p></li>
    </ol>
  <p>Laptop  hendaklah disediakan sendiri oleh peserta. </p> </li>
  <li><p>Bahan  yang menggunakan bahan letupan atau berbahaya adalah dilarang sepanjang  pertandingan ini.</p></li>
</ol>


<strong><p>Poster / Bunting </p></strong>
<ol>
  <li><p>Bunting (jenis gantung) perlu disediakan oleh peserta sendiri untuk setiap penyertaan.</p></li>
  <li><p>Kandungan penyelidikan untuk penyediaan poster peserta mestilah sesuai untuk dimuatkan dalam satu bunting berukuran (Saiz A1): 841 mm (Tinggi) x 594 mm (Lebar) atau 33.1 in (Tinggi) x 23.4 in (Lebar). Oleh itu hadkan gambar rajah dan penerangan.</p></li>
  <li><p>Adalah dicadangkan kandungan bunting menitikberatkan (highlight) ciri-ciri produk penyelidikan seperti ‘abstract, problem statement, objectives, novelty, usefulness, commercialization potential, benefit to society and recognition received’ (abstrak, pernyataan masalah, objektif, keaslian, kebolehgunaan, potensi dikomersilkan, kebaikan kepada masyarakat, pengiktirafan diterima) tentang penyelidikan masing-masing.</p></li>
  <li><p>Tajuk penyelidikan, nama-nama ahli, fakulti, cawangan / bahagian dan logo organisasi atau institusi perlu dimuatkan sebagai sebahagian dari kandungan poster (bahagian atas kandungan poster).</p></li>
  <li><p>Selain dari poster, peserta adalah digalakkan untuk menyediakan ‘brochure’, penghargaan, gambar dan lain-lain yang berkaitan.</p></li>
</ol>

<h4>SYARAT PENYERTAAN</h4>
<hr />
<ol>
<li><p>Penyertaan adalah terbuka kepada semua kakitangan (akademik & bukan akademik), pelajar IPTA & IPTS & pelajar sekolah menengah di seluruh negara yang mempunyai daya kreativiti dalam penciptaan produk inovasi.</p></li>
<li><p>Bagi penyelidikan/produk yang mempunyai hubungan kerjasama dengan industri luar adalah digalakkan untuk menyertai pertandingan ini dengan syarat kedua -dua belah pihak telah mempersetujui isu berhubung perlindungan harta intelek (IP). </p></li>
<li><p>Pihak penganjur juga mempunyai hak untuk menolak sebarang permohonan yang dihantar. </p></li>
<li><p>Penyertaan dibenarkan secara INDIVIDU atau secara KUMPULAN (maksimum 5 orang sahaja bagi satu kumpulan)</p></li>
<li><p>Kategori pertandingan adalah dibuka seperti berikut:</p></li>
<dl class="dl-horizontal">
  <dt>Invention</dt>
  <dd>Rekaan baru produk (barangan & perkhidmatan), sistem atau proses yang tidak pernah dihasilkan sebelum ini.</dd>
   <dt>Innovation</dt>
  <dd>Produk (barangan & perkhidmatan), sistem atau proses yang ditambahbaik.</dd>
   <dt>Design</dt>
  <dd>Rekabentuk, hasil visual, hasil seni (artwork) yang baru.</dd>
  </dl>

